    private void initOwn(){
        btnSplit.setEnabled(false);
        
        GIS.Lock();
        
        GIS.Open(TGIS_Utils.GisSamplesDataDirDownload() + "/Samples/Topology/topology3.shp");
        
        layerPolygon = (TGIS_LayerVector)GIS.getItems().getItems(0);
        shpPolygon = (TGIS_ShapePolygon)layerPolygon.GetShape(1).MakeEditable();
        if(shpPolygon == null) return;
        
        layerArc = new TGIS_LayerVector();
        layerArc.getParams().getLine().setColor(TGIS_Color.getRed());
        layerArc.getParams().getLine().setWidth(25);
        if(layerArc == null) return;
        GIS.Add(layerArc);
        
        shpArc = (TGIS_ShapeArc)((TGIS_LayerVector)GIS.getItems().getItems(1)).CreateShape(TGIS_ShapeType.Arc);
        if(shpArc == null) return;
        shpArc.AddPart();
        
        layerObj = new TGIS_LayerVector();
        layerObj.setName("Splits");
        GIS.Add(layerObj);
        
        GIS.Unlock();
        
        GIS.FullExtent();
        GIS.setRestrictedExtent(GIS.getExtent());
    }
