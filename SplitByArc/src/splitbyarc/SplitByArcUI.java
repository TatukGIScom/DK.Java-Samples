    private void btnLineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLineActionPerformed
        layerObj.RevertShapes();
        shpArc.Reset();
        shpArc.AddPart();
        taShapeInfo.setText("...");
        GIS.InvalidateWholeMap();
        btnSplit.setEnabled(false);
    }//GEN-LAST:event_btnLineActionPerformed

    private void btnSplitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSplitActionPerformed
        int n;
        TGIS_Topology topologyObj;
        TObjectList<Object> shape_list;
        Random random;
        TGIS_Shape tmp;
        layerObj.RevertShapes();
        topologyObj = new TGIS_Topology();
        
        shape_list =  topologyObj.SplitByArc(shpPolygon, shpArc, true);
        if(shape_list != null){
            taShapeInfo.setText("Shapes after split: \n" + shape_list.getCount());
            random = new Random();
            for(n = 0; n < shape_list.getCount()  ; n++){
                ((TGIS_Shape)shape_list.getItems(n)).getParams().getArea().setColor(TGIS_Color.FromRGB(random.nextInt(255 ) * 256 * 256 + random.nextInt( 255 ) * 256 + random.nextInt( 256 )));
                tmp = (TGIS_Shape)shape_list.getItems(n);
                layerObj.AddShape((TGIS_Shape)shape_list.getItems(n));
            }
        GIS.InvalidateWholeMap();
        }
    }//GEN-LAST:event_btnSplitActionPerformed

    private void GISMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_GISMousePressed
        TGIS_Point ptg;
        
        if(GIS.getIsEmpty()) return;
        ptg = GIS.ScreenToMap(new TPoint(evt.getX(), evt.getY()));
        shpArc.Lock(TGIS_Lock.Extent);
        shpArc.AddPoint(ptg);
        shpArc.Unlock();
        GIS.InvalidateWholeMap();
        if(shpArc.Intersect(shpPolygon)) 
            btnSplit.setEnabled(true);
    }//GEN-LAST:event_GISMousePressed

    private void GISMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_GISMouseReleased
        TGIS_Point ptg;
        
        if(GIS.getIsEmpty()) return;
        
        ptg = GIS.ScreenToMap(new TPoint(evt.getX(), evt.getY()));
        shpArc.Lock(TGIS_Lock.Extent);
        shpArc.AddPoint(ptg);
        shpArc.Unlock();
        GIS.InvalidateWholeMap();
        if(shpArc.Intersect(shpPolygon)) btnSplit.setEnabled(true);
    }//GEN-LAST:event_GISMouseReleased


